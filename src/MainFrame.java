    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (!semesters.isEmpty())
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("Add a semester.");
            currentSemester = "None";
        }
    }
    
    private void rebuildCurrentStudentCourseComboBox()
    {
        String currentSemester = currentSemesterComboBox.getSelectedItem().toString();
        String selectedStudent = selectedStudentScheduleComboBox.getSelectedItem().toString();
        String last = selectedStudent.split(",")[0];
        String first = selectedStudent.split(",")[1];
        
        ArrayList<StudentEntry> student = StudentQueries.getSingleStudent(first, last);
        String id = student.get(0).getStudentID();
       
        ArrayList<ScheduleEntry> courses = ScheduleQueries.getScheduleByStudent(currentSemester, id);
        String[] coursesArr = new String[courses.size()];
        
        int i = 0;
        for (ScheduleEntry course : courses)
        {
            coursesArr[i] = course.getCourseCode();
            i++;
        }
    }
    
