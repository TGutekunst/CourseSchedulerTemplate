    private void adminDropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropCourseButtonActionPerformed
        adminDropCourseTextArea.setText("");
        
        String scheduledStudents = "Scheduled students dropped from the course:\n";
        String waitlistedStudents = "Waitlisted students dropped from the course:\n";
        
        StudentEntry currStudent;

        for (ScheduleEntry currentEntry : ScheduleQueries.getScheduledStudentsByCourse(currentSemesterComboBox.getSelectedItem().toString(), adminDropCourseComboBox.getSelectedItem().toString()))
        {
            currStudent = StudentQueries.getStudent(currentEntry.getStudentID());
            scheduledStudents += currStudent.getLastName() + ", " + currStudent.getFirstName() + " " + currStudent.getStudentID() + "\n"; 
        }
        for (ScheduleEntry currentEntry : ScheduleQueries.getWaitlistedStudentsByCourse(currentSemesterComboBox.getSelectedItem().toString(), adminDropCourseComboBox.getSelectedItem().toString()))
        {
            currStudent = StudentQueries.getStudent(currentEntry.getStudentID());
            waitlistedStudents += currStudent.getLastName() + ", " + currStudent.getFirstName() + " " + currStudent.getStudentID() + "\n"; 
        }

        ScheduleQueries.dropScheduleByCourse(currentSemesterComboBox.getSelectedItem().toString(), adminDropCourseComboBox.getSelectedItem().toString());
        CourseQueries.dropCourse(currentSemesterComboBox.getSelectedItem().toString(), adminDropCourseComboBox.getSelectedItem().toString());
        adminDropCourseTextArea.append(scheduledStudents + "\n");
        adminDropCourseTextArea.append(waitlistedStudents);
        
        rebuildCoursesComboBoxes();
    }//GEN-LAST:event_adminDropCourseButtonActionPerformed

    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
          String currentSemester = currentSemesterComboBox.getSelectedItem().toString();
        
        dropStudentTextArea.setText("");
        String selectedStudent = dropStudentComboBox.getSelectedItem().toString();
        String lastName = selectedStudent.split(", ")[0];
        String firstName = selectedStudent.split(",")[1];
        ArrayList<StudentEntry> student = StudentQueries.getSingleStudent(firstName, lastName);
        String id = student.get(0).getStudentID();
        
        StudentEntry currentStudent = StudentQueries.getStudent(id);
        
        dropStudentTextArea.append(dropStudentComboBox.getSelectedItem().toString() + " has been dropped from list\n\n");

        for (String currSemester : SemesterQueries.getSemesterList())
        {
            String drop = "";
            String scheduled = "";
            for (ScheduleEntry currentEntry : ScheduleQueries.getScheduleByStudent(currSemester, currentStudent.getStudentID()))
            {
                ScheduleQueries.dropStudentScheduleByCourse(currSemester, currentStudent.getStudentID(), currentEntry.getCourseCode());
                if(currentEntry.getStatus().equals("w")){
                    drop += (dropStudentComboBox.getSelectedItem().toString() +" has been dropped from the waitlist for "+currentEntry.getCourseCode() + "\n");
                
                }
                else{
                    drop += (dropStudentComboBox.getSelectedItem().toString() +" has been dropped from "+currentEntry.getCourseCode() + "\n");
               
                }                
                ArrayList<ScheduleEntry> wait = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, currentEntry.getCourseCode());
                if (!wait.isEmpty())
                {
                    ScheduleQueries.updateScheduleEntry(currSemester, wait.get(0));
                    StudentEntry waitlistedStu = StudentQueries.getStudent(wait.get(0).getStudentID());
                    scheduled += waitlistedStu.getLastName() + ", " + waitlistedStu.getLastName() + " "+ wait.get(0).getStudentID() + " has been scheduled into " + wait.get(0).getCourseCode() + "\n";
                }
            }
            
            if (!drop.isEmpty()||!scheduled.isEmpty())
            {
                dropStudentTextArea.append("For Semester: " + currSemester + "\n");
                dropStudentTextArea.append(drop);
                dropStudentTextArea.append(scheduled);
            }
        }
        StudentQueries.dropStudent(id);
        rebuildSelectedStudentComboBoxes(); 
        rebuildCurrentStudentCourseComboBox();
        rebuildStudentComboBoxes();
        rebuildCoursesComboBoxes();
    }//GEN-LAST:event_dropStudentButtonActionPerformed

    private void displayListOfStudentsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayListOfStudentsButtonActionPerformed
        ArrayList<ScheduleEntry> scheduledStudentsSchedules =  ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseListOfStudentsComboBox.getSelectedItem().toString());
        ArrayList<ScheduleEntry> waitlistedStudentsSchedules =  ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseListOfStudentsComboBox.getSelectedItem().toString());
        ArrayList<StudentEntry> scheduledStudents = new ArrayList<StudentEntry>();
        ArrayList<StudentEntry> waitlistedStudents = new ArrayList<StudentEntry>();

        for (ScheduleEntry schedule: scheduledStudentsSchedules){
            scheduledStudents.add(StudentQueries.getStudent(schedule.getStudentID()));
        }
        
        for (ScheduleEntry schedule: waitlistedStudentsSchedules){
            waitlistedStudents.add(StudentQueries.getStudent(schedule.getStudentID()));
        }
        DefaultTableModel displayScheduledStudentsTable = (DefaultTableModel) scheduledStudentsTable.getModel();
        displayScheduledStudentsTable.setNumRows(0);
        for (StudentEntry student: scheduledStudents){
            displayScheduledStudentsTable.addRow(new Object[]{student.getLastName(), student.getFirstName(), student.getStudentID()});
        }       
        
        DefaultTableModel displayWaitlistedStudentsTable = (DefaultTableModel) waitlistedStudentsTable.getModel();
        displayWaitlistedStudentsTable.setNumRows(0);
        for (StudentEntry student: waitlistedStudents){
            displayWaitlistedStudentsTable.addRow(new Object[]{student.getLastName(), student.getFirstName(), student.getStudentID()});
        }
    }//GEN-LAST:event_displayListOfStudentsButtonActionPerformed

